package com.meta.cj.day17;


class Clac{

    int a;
    int b;
    float c;
    float d;

    public Clac(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public Clac(int b, float c) {
        this.b = b;
        this.c = c;
    }

    public Clac(float c, float d) {
        this.c = c;
        this.d = d;
    }

    public Clac(int a, int b, float c, float d) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
    }
}
